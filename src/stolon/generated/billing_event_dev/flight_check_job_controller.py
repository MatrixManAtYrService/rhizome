    # Parse response body
    import json
    if proxy_response.body and proxy_response.status_code == 200:
        try:
            body_json = json.loads(proxy_response.body)
            return CheckResponse.from_dict(body_json)
        except (json.JSONDecodeError, KeyError, TypeError):
            pass
    return None

